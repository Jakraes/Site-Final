function countTime(time) {
    let minutes = Math.floor(time / 60000);
    let seconds = ((time % 60000) / 1000).toFixed(0);
    if (seconds < 10){
        seconds = "0"+seconds
    }
    else{
        seconds = seconds
    }
    return [minutes + ":" + seconds, time];
}
